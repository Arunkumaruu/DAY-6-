    // Email validation function
    function validateEmail() {
        const email = $('#email').val();
        const emailPattern = /^[^\s@]+@[^\s@]+\.[^\s@]+$/;
        if (!emailPattern.test(email)) {
            $('#emailFeedback').text('Please enter a valid email address.').css('color', 'red');
        } else {
            $('#emailFeedback').text('');
        }
    }
    //FULL NAME VALIDATION
     
    function validateFullname() {
        const name = $('#fullName').val();
        const namePattern = /^[a-zA-Z\s]+$/;
        if (!namePattern.test(name)) {
            $('#nameFeedback').text('Please enter a valid name.').css('color', 'red');
        } else {
            $('#nameFeedback').text('');
        }
    }
//USER-NAME VALIDATION
function validateUserName() {
    const name = $('#username').val();
    const namePattern = /^[a-zA-Z\s]+$/ ;
    if (!namePattern.test(name)) {
        $('#usernameFeedback').text('Please enter a valid name.').css('color', 'red');
    } else {
        $('#usernameFeedback').text('');
    }
}

  // Address validation function
  function validateAddress(id, feedbackId) {
    const address = $(`#${id}`).val();
    const addressPattern = /^[a-zA-Z0-9\s,'-]*$/; // Example pattern
    if (!addressPattern.test(address) || address.trim() === '') {
        $(`#${feedbackId}`).text('Please enter a valid address.').css('color', 'red');
    } else {
        $(`#${feedbackId}`).text('');
    }
}


    // Phone number validation function
  
        function validatePhone() {
            const phone = $('#phone').val();
            const phonePattern =  /^[6-9]\d{9}$/; // Phone number must start with 6 and be followed by 9 digits
        
            if (!phonePattern.test(phone)) {
                if (phone.length !== 10) {
                    $('#phoneFeedback').text('Phone number must be exactly 10 digits.').css('color', 'red');
                } else if (!phone.startsWith('6')) {
                    $('#phoneFeedback').text('Phone number must start with the digit 6 to 9.').css('color', 'red');
                } else {
                    $('#phoneFeedback').text('Phone number is invalid.').css('color', 'red');
                }
            } else {
                $('#phoneFeedback').text('Phone number is valid.').css('color', 'green');
            }
        }

    // Password strength check
    function checkPasswordStrength(password) {
        let strength = {
            message: 'Weak',
            color: '#ff0000' // Red
        };

        if (password.length >= 8) {
            strength.message = 'Medium';
            strength.color = 'blue'; // Blue
        }

        if (password.length >= 12 && /[A-Z]/.test(password) && /[0-9]/.test(password) && /[!@#$%^&*]/.test(password)) {
            strength.message = 'Strong';
            strength.color = '#00ff00'; // Green
        }

        return strength;
    }

    // Password validation
    function validatePasswords() {
        if ($('#password').val() === $('#confirmPassword').val()) {
            $('#password-message').text('Passwords match').css('color', 'green');
        } else {
            $('#password-message').text('Passwords do not match').css('color', 'red');
        }
    }

    // Update districts based on selected state
    function updateDistricts() {
        const selectedState = $('#states').val();
        $('#districts').html('<option value="">Select district</option>');
        if (districts[selectedState]) {
            districts[selectedState].forEach(district => {
                $('#districts').append(`<option value="${district}">${district}</option>`);
            });
        }
    }

    // Age calculation based on birthdate
    function calculateAge() {
        const birthdate = $('#birthdate').val();
        if (!birthdate) {
            $('#age').text('');
            $('#restriction').text('');
            return;
        }
        const birthDate = new Date(birthdate);
        const today = new Date();
        let age = today.getFullYear() - birthDate.getFullYear();
        const monthDifference = today.getMonth() - birthDate.getMonth();
        if (monthDifference < 0 || (monthDifference === 0 && today.getDate() < birthDate.getDate())) {
            age--;
        }
        $('#age').text('Your age is: ' + age);

        if (birthDate > today) {
            $('#restriction').text('Future date not allowed');
            $('#age').text('');
        } else if (age < 18) {
            $('#restriction').text('You are under 18. Access is restricted.');
        } else {
            $('#restriction').text('');
        }
    }

